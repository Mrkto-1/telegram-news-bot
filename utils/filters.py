# –§–∞–π–ª —Å—Ç–≤–æ—Ä–µ–Ω–æ –¥–ª—è –∫–∞–Ω–∞–ª—É "–°–≤—ñ—Ç–æ–≤—ñ –Ω–æ–≤–∏–Ω–∏ —â–æ–≥–æ–¥–∏–Ω–∏! üåç"
# –§—É–Ω–∫—Ü—ñ—è —Ñ—ñ–ª—å—Ç—Ä–∞—Ü—ñ—ó –Ω–µ–ø–æ—Ç—Ä—ñ–±–Ω–∏—Ö —Ç–µ–º –∑—ñ —Å–ø–∏—Å–∫—É –Ω–æ–≤–∏–Ω
# –Ø–∫—â–æ —Ç—Ä–µ–±–∞ –¥–æ–¥–∞—Ç–∏ –Ω–æ–≤—É —Ç–µ–º—É –¥–æ —á–æ—Ä–Ω–æ–≥–æ —Å–ø–∏—Å–∫—É ‚Äî –ø—Ä–æ—Å—Ç–æ –¥–æ–¥–∞–π —ó—ó –¥–æ BLACKLIST

# –°–ø–∏—Å–æ–∫ —Ç–µ–º, —è–∫—ñ –Ω–µ –ø—É–±–ª—ñ–∫—É—é—Ç—å—Å—è
BLACKLIST = [
    "—Å–ø–æ—Ä—Ç",
    "–º–æ–¥–∞",
    "–º—É–∑–∏–∫–∞",
    "–∫—ñ–Ω–æ",
    "—Ä–æ–∑–≤–∞–≥–∏",
    "—Å–µ—Ä—ñ–∞–ª–∏",
    "—Ñ—É—Ç–±–æ–ª",
    "—à–æ—É-–±—ñ–∑–Ω–µ—Å",
    "–≥–æ—Ä–æ—Å–∫–æ–ø",
    "–∑–Ω–∞–º–µ–Ω–∏—Ç–æ—Å—Ç—ñ",
    "—ñ–≥—Ä–∏"
]

def is_relevant(title: str, summary: str = "") -> bool:
    """
    –ü–µ—Ä–µ–≤—ñ—Ä—è—î, —á–∏ –Ω–æ–≤–∏–Ω–∞ –Ω–µ –º—ñ—Å—Ç–∏—Ç—å –∑–∞–±–æ—Ä–æ–Ω–µ–Ω—ñ —Ç–µ–º–∏.
    :param title: –ó–∞–≥–æ–ª–æ–≤–æ–∫ –Ω–æ–≤–∏–Ω–∏
    :param summary: –û–ø–∏—Å/–∑–º—ñ—Å—Ç –Ω–æ–≤–∏–Ω–∏
    :return: True —è–∫—â–æ –Ω–æ–≤–∏–Ω–∞ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–∞, False —è–∫—â–æ —Å–ª—ñ–¥ –≤—ñ–¥–∫–∏–Ω—É—Ç–∏
    """
    combined_text = (title + " " + summary).lower()
    return not any(word in combined_text for word in BLACKLIST)
